a, b = 7 , 5

# Operador de igualdad

resultado = a == b

print(f"Resultado de a==b es {resultado}")

# Operador de comparacion (son diferentes...?)

resultado = a != b

print(f"Resultado de a!=b es {resultado}")

# Operador de menor igual

resultado = a <= b

print(f"Resultado de a<=b es {resultado}")

resultado = a >= b

print(f"Resultado de a>=b es {resultado}")

